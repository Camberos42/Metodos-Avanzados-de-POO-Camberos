package facade;

public class CDplayer {
	
	public void On(){
		System.out.println("CDplayer On");
	}

	public void Off(){

		System.out.println("CDplayer OFF");

	}

	
	public void Stop(){		
		System.out.println("Stop");


	}

	public void Eject(){
		System.out.println("Ejecting CD");
	}
	
	public void Insert(){
		System.out.println("Reading CD.");

	}

	public void Play(){
		System.out.println("Play");


	}

}
