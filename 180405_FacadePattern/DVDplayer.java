package facade;

public class DVDplayer {
	
	public void On(){
		System.out.println("DVD ON");
	}

	public void Off(){

		System.out.println("DVD OFF");

	}

	public void Insert(){
		System.out.println("Reading DVD");

	}
	
	public void Stop(){		
		System.out.println("Stop.");


	} 
	
	public void Eject(){
		System.out.println("Ejecting DVD");

	}

	public void Play(){
		System.out.println("Play.");


	}


}
